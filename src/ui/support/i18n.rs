#[derive(Component)]
pub(crate) struct LocalizedText(pub(crate) UiText);

pub(crate) fn sync(settings: Res<RuntimeSettings>, mut labels: Query<(&LocalizedText, &mut Text)>) {
    for (label, mut text) in &mut labels {
        let localized = tr(settings.locale, label.0);
        if text.0 != localized {
            text.0 = localized.into();
        }
    }
}

