pub(crate) fn overwrite_slot(locale: UiLocale, slot: u32) -> String {
    match locale {
        UiLocale::ZhCn => format!("覆盖存档 {slot:02}？"),
        UiLocale::ZhTw => format!("覆蓋存檔 {slot:02}？"),
        UiLocale::Ja => format!("スロット {slot:02} を上書きしますか？"),
        UiLocale::En => format!("Overwrite slot {slot:02}?"),
    }
}

pub(crate) fn load_slot(locale: UiLocale, slot: u32) -> String {
    match locale {
        UiLocale::ZhCn => format!("读取存档 {slot:02}？"),
        UiLocale::ZhTw => format!("讀取存檔 {slot:02}？"),
        UiLocale::Ja => format!("スロット {slot:02} をロードしますか？"),
        UiLocale::En => format!("Load slot {slot:02}?"),
    }
}

pub(crate) fn delete_slot(locale: UiLocale, slot: u32) -> String {
    match locale {
        UiLocale::ZhCn => format!("删除存档 {slot:02}？"),
        UiLocale::ZhTw => format!("刪除存檔 {slot:02}？"),
        UiLocale::Ja => format!("スロット {slot:02} を削除しますか？"),
        UiLocale::En => format!("Delete slot {slot:02}?"),
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn exposes_the_webgal_k_locale_set() {
        assert_eq!(UiLocale::ALL.len(), 4);
        assert_eq!(UiLocale::ZhCn.native_name(), "简体中文");
        assert_eq!(UiLocale::ZhTw.native_name(), "繁體中文");
        assert_eq!(UiLocale::En.native_name(), "English");
        assert_eq!(UiLocale::Ja.native_name(), "日本語");
    }

    #[test]
    fn translates_menu_routes_without_touching_project_title_copy() {
        assert_eq!(tr(UiLocale::ZhCn, UiText::System), "系统");
        assert_eq!(tr(UiLocale::ZhCn, UiText::Title), "标题画面");
        assert_eq!(tr(UiLocale::Ja, UiText::System), "システム");
        assert_eq!(tr(UiLocale::En, UiText::System), "SYSTEM");
        // START / CONTINUE / EXTRA / OPTIONS / EXIT remain project-facing
        // title-screen copy. Title here is only the shared menu route label.
    }
}
